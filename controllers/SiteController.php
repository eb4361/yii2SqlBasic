		// Solution where we use SQL to calculate percentage:
		$connection = Yii::$app->db;
		$command = $connection->createCommand('SELECT COUNT(*)*100 / (SELECT COUNT(*) FROM tom_task WHERE tom_task.project_id = 1)
												FROM tom_task
												WHERE tom_task.project_id = 1 AND tom_task.id IN (
